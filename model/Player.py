

class Player:
    __name
    __lastName
    __email

    def getName(self):
        return self.__name

    def setName(self, name):
        self.name = name

    def getLastName(self):
        return self.__lastName
    
    def setLastName(self, lastName):
        self.__lastName = lastName
    
    def getEmail(self):
        return self.__email

    def setEmail(self, email):
        self.email = email